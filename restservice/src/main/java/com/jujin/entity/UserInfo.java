package com.jujin.entity;

/**
 * 个人资料详情(一般用于借款人)
 */
public class UserInfo extends UserNickName {

	/*真实姓名*/
	private String realName;

	/*1:男;2:女*/
	private String gender;
	
	/*年龄*/
	private String age;
	
	/*教育程度*/
	private String education;
	
	/*月收入*/
	private String monthlyIncome;
	
	/*婚姻状况*/
	private String maritalStatus;
	
	/*社会保险*/
	private String  socialInsurance;
	
	/*是否有住房*/
	private String  hasHousing;
	
	/*是否购车*/
	private String hasCar;

	/**
	 * @return the realName
	 */
	public String getRealName() {
		return realName;
	}

	/**
	 * @param realName the realName to set
	 */
	public void setRealName(String realName) {
		this.realName = realName;
	}

	/**
	 * @return the gender
	 */
	public String getGender() {
		return gender;
	}

	/**
	 * @param gender the gender to set
	 */
	public void setGender(String gender) {
		this.gender = gender;
	}

	/**
	 * @return the age
	 */
	public String getAge() {
		return age;
	}

	/**
	 * @param age the age to set
	 */
	public void setAge(String age) {
		this.age = age;
	}

	/**
	 * @return the education
	 */
	public String getEducation() {
		return education;
	}

	/**
	 * @param education the education to set
	 */
	public void setEducation(String education) {
		this.education = education;
	}

	/**
	 * @return the monthlyIncome
	 */
	public String getMonthlyIncome() {
		return monthlyIncome;
	}

	/**
	 * @param monthlyIncome the monthlyIncome to set
	 */
	public void setMonthlyIncome(String monthlyIncome) {
		this.monthlyIncome = monthlyIncome;
	}

	/**
	 * @return the maritalStatus
	 */
	public String getMaritalStatus() {
		return maritalStatus;
	}

	/**
	 * @param maritalStatus the maritalStatus to set
	 */
	public void setMaritalStatus(String maritalStatus) {
		this.maritalStatus = maritalStatus;
	}

	/**
	 * @return the socialInsurance
	 */
	public String getSocialInsurance() {
		return socialInsurance;
	}

	/**
	 * @param socialInsurance the socialInsurance to set
	 */
	public void setSocialInsurance(String socialInsurance) {
		this.socialInsurance = socialInsurance;
	}

	/**
	 * @return the hasHousing
	 */
	public String getHasHousing() {
		return hasHousing;
	}

	/**
	 * @param hasHousing the hasHousing to set
	 */
	public void setHasHousing(String hasHousing) {
		this.hasHousing = hasHousing;
	}

	/**
	 * @return the hasCar
	 */
	public String getHasCar() {
		return hasCar;
	}

	/**
	 * @param hasCar the hasCar to set
	 */
	public void setHasCar(String hasCar) {
		this.hasCar = hasCar;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

}
