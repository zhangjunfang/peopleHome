public class ZhaiQuan {
	
	
	private String borrowId;
	
	private String borrowTtile;
	
	private String borrowType;
	
	private String tenderSeq;
	 
	private String validAccountTender;
	
	
	

	public String getBorrowId() {
		return borrowId;
	}

	public void setBorrowId(String borrowId) {
		this.borrowId = borrowId;
	}

	public String getBorrowTtile() {
		return borrowTtile;
	}

	public void setBorrowTtile(String borrowTtile) {
		this.borrowTtile = borrowTtile;
	}

	public String getBorrowType() {
		return borrowType;
	}

	public void setBorrowType(String borrowType) {
		this.borrowType = borrowType;
	}

	public String getTenderSeq() {
		return tenderSeq;
	}

	public void setTenderSeq(String tenderSeq) {
		this.tenderSeq = tenderSeq;
	}

	public String getValidAccountTender() {
		return validAccountTender;
	}

	public void setValidAccountTender(String validAccountTender) {
		this.validAccountTender = validAccountTender;
	}
	
	
	
	
	
	

}
